// Simple function to get translated text
export const getText = (language, key) => {
  const keys = key.split('.');
  let result = translations[language] || translations['en'];
  
  for (const k of keys) {
    if (!result || typeof result !== 'object') return key;
    result = result[k];
  }
  
  return result || key;
}; 